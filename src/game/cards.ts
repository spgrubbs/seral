export function getCardById(id: string): Card | undefined {
  return ALL_CARDS.find(c => c.id === id);
}

export function getStarterDeck(): Card[] {
  const starterIds = [
    'crust-lichen', 'crust-lichen', 'crust-lichen',
    'spore-moss', 'spore-moss',
    'pioneer-fern', 'pioneer-fern',
    'blue-green-algae',
    'mycorrhizal-fungi',
    'n-fixing-shrub', 'n-fixing-shrub',
    'tussock-grass', 'tussock-grass',
    'wildflower-meadow',
    'pollinator-bee',
    'decomposer-guild',
    'drainage-redirect',
    'mineral-amendment',
    'good-rain-year',
    'warm-winter',
  ];
  return starterIds.map(id => {
    const card = getCardById(id);
    if (!card) throw new Error(`Card not found: ${id}`);
    return { ...card };
  });
}
